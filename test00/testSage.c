    #include "minc_tides.h"

    void    mincseries(double t,double *v, double *p, double **XVAR,int ORDER, int MO)
    {
    int VAR,PAR,TT,i,j, inext;
    	VAR = 4;
	PAR = 0;
	TT = 36;

    double XX[TT+1][MO+1];

    for(j=0; j<=TT; j++)
        for(i=0; i<=ORDER; i++)
            XX[j][i] = 0.e0;
    XX[0][0] = t;
    XX[0][1] = 1.e0;
    for(i=1;i<=VAR;i++) {
        XX[i][0] = v[i-1];
    }

    for(i=0;i<ORDER;i++) {
    		XX[4][i] = 30.000000000000000*XX[1][i];
		XX[5][i] = (i==0)? 2.1000000000000001+XX[4][0] : XX[4][i];
		XX[6][i] = mul_mc(XX[3],XX[3],i);
		XX[7][i] = mul_mc(XX[6],XX[5],i);
		XX[8][i] = -2.0000000000000000*XX[7][i];
		XX[9][i] = -16.000000000000000*XX[1][i];
		XX[10][i] = XX[9][i] + XX[8][i];
		XX[11][i] = 160.00000000000000*XX[1][i];
		XX[12][i] = (i==0)? -7.1999999999999993+XX[11][0] : XX[11][i];
		XX[13][i] = mul_mc(XX[2],XX[12],i);
		XX[14][i] = -150.00000000000000*XX[1][i];
		XX[15][i] = (i==0)? -4.5750000000000002+XX[14][0] : XX[14][i];
		XX[16][i] = exp_mc(XX[15],XX[16], i);
		XX[17][i] = (i==0)? 1.0000000000000000+XX[16][0] : XX[16][i];
		XX[18][i] = pow_mc_c(XX[17],-3.0000000000000000,XX[18], i);
		XX[19][i] = mul_mc(XX[18],XX[13],i);
		XX[20][i] = -2.0000000000000000*XX[19][i];
		XX[21][i] = XX[20][i] + XX[10][i];
		XX[22][i] = (i==0)? -0.74800000000000000+XX[21][0] : XX[21][i];
		XX[23][i] = -24.691358024691358*XX[2][i];
		XX[24][i] = 500.00000000000000*XX[1][i];
		XX[25][i] = (i==0)? 16.250000000000000+XX[24][0] : XX[24][i];
		XX[26][i] = exp_mc(XX[25],XX[26], i);
		XX[27][i] = (i==0)? 1.0000000000000000+XX[26][0] : XX[26][i];
		XX[28][i] = inv_mc(24.691358024691358,XX[27],XX[28], i);
		XX[29][i] = XX[28][i] + XX[23][i];
		XX[30][i] = -1.1111111111111112*XX[3][i];
		XX[31][i] = -83.000000000000000*XX[1][i];
		XX[32][i] = (i==0)? 0.078850000000000239+XX[31][0] : XX[31][i];
		XX[33][i] = exp_mc(XX[32],XX[33], i);
		XX[34][i] = (i==0)? 1.0000000000000000+XX[33][0] : XX[33][i];
		XX[35][i] = inv_mc(1.1111111111111112,XX[34],XX[35], i);
		XX[36][i] = XX[35][i] + XX[30][i];
		XX[1][i+1] = XX[22][i] / (i+1.0);
		XX[2][i+1] = XX[29][i] / (i+1.0);
		XX[3][i+1] = XX[36][i] / (i+1.0);
	}

	for(j=0; j<=VAR; j++)
		for(i=0; i<=ORDER; i++)
			XVAR[i][j] = XX[j][i];
}
